/**
 * 创建 DOM 元素的便捷函数
 * @param {string} tag
 * @param {string} className
 * @param {string} [html]
 */
function el(tag, className, html) {
  const node = document.createElement(tag);
  if (className) node.className = className;
  if (html !== undefined) node.innerHTML = html;
  return node;
}
